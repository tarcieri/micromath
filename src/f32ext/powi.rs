use super::utils;
/// x^n where n is an integer
use crate::f32ext::utils::FloatComponents;
use core::f32;

pub(super) fn powi_exp_by_squaring(x: f32, n: i32) -> f32 {
    // source: https://stackoverflow.com/a/101613
    // note not optimal for all powers, is NP complete
    // https://en.wikipedia.org/wiki/Addition-chain_exponentiation
    // will result in an addition multiplication for x^15 for example
    let mut base: f32 = x;
    let mut abs_n: i32 = i32::abs(n);
    let mut result: f32 = 1.0_f32;

    if n < 0_i32 {
        base = 1.0 / x;
    }

    if n == 0 {
        return 1.0_f32;
    }
    //0.0_f32 == 0.0_f32 and -0.0_f32 according to ieee standards.
    if x == 0.0_f32 && n > 0 {
        return x;
    }
    //for values less than 2.0, but greater than 0.5 (1.0/2.0), you can multiply longer with out going over exponent, ie 1.1 multiplied against itself will grow slowly.
    if abs::abs(x) >= 2.0 && abs::abs(x) <= 0.5 {
        //Approximation if we end up outside of the range of floating point values, then we end early
        let approx_final_exponent: i32 = x.extract_exponent_value() * n;
        let max_representable_exponent = 127;
        let min_representable_exponent = -126 - (utils::MANTISSA_BITS as i32);
        if approx_final_exponent > max_representable_exponent
            || (x == 0.0_f32 && approx_final_exponent < 0)
        {
            if x.is_sign_positive() || n & 1 == 0 {
                return f32::INFINITY;
            } else {
                return f32::NEG_INFINITY;
            }
        } else if approx_final_exponent < min_representable_exponent {
            // We may want to copy the sign and do the same thing as above,
            // but that seems like an awful amount of work when 99.99999% of people only care
            // about bare zero
            return 0.0_f32;
        }
    }
    loop {
        if (abs_n & 1) == 1 {
            result *= base;
        }
        abs_n >>= 1;
        if abs_n == 0_i32 {
            break;
        }
        base *= base;
    }
    result
}
